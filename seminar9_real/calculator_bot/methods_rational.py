import math


def sum(num_one, num_two):
    return num_one + num_two


def substraction(num_one, num_two):
    return num_one - num_two


def multiplication(num_one, num_two):
    return num_one * num_two


def division(num_one, num_two):
    return num_one / num_two


def power(num_one, num_two):
    return math.pow(num_one, num_two)
